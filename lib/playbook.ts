async function pdfBlob(node: React.ReactElement): Promise<Blob> {
  const { pdf } = await import("@react-pdf/renderer");
  return await pdf(node).toBlob();
}

function triggerDownload(blob: Blob, filename: string) {
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  setTimeout(() => URL.revokeObjectURL(url), 1000);
}

export async function downloadBrandPlaybook(g: GeneratedBrand) {
  const JSZip = (await import("jszip")).default;
  const { buildBrandPlaybook } = await import("@/components/PlaybookPDF");

  const zip = new JSZip();
  const base = safeName(g.input.businessName) || "brand";

  // README
  zip.file("README.md", buildBrandReadme(g));

  // PDF
  const pdfNode = buildBrandPlaybook(g, languageNative(g.input.outputLanguage));
  const blob = await pdfBlob(pdfNode);
  zip.file(`${base}-playbook.pdf`, blob);

  // Assets folder
  const assets = zip.folder("assets")!;

  // Logo
  if (g.logoImageDataUrl) {
    assets.file(`logo.${inferExt(g.logoImageDataUrl)}`, dataUrlToBlob(g.logoImageDataUrl));
  }

  // Mockups
  g.mockupImages.forEach((img, i) => {
    if (img) {
      assets.file(
        `mockup-${String(i + 1).padStart(2, "0")}.${inferExt(img)}`,
        dataUrlToBlob(img)
      );
    }
  });

  // Concept thumbnails (the directions that were considered)
  g.palettes.forEach((p, i) => {
    if (p.conceptImageDataUrl) {
      assets.file(
        `concept-${String(i + 1).padStart(2, "0")}-${safeName(p.name)}.${inferExt(p.conceptImageDataUrl)}`,
        dataUrlToBlob(p.conceptImageDataUrl)
      );
    }
  });

  // Color tokens
  assets.file("colors.css", buildColorsCss(g.selectedPalette));
  assets.file("colors.json", buildColorsJson(g.selectedPalette));

  // Type
  assets.file("type.md", buildTypeReadme(g.selectedType));

  // Data dump (for re-import)
  zip.file("brand.json", JSON.stringify(g, null, 2));

  const out = await zip.generateAsync({ type: "blob" });
  triggerDownload(out, `${base}-playbook.zip`);
}


/** Convert a data URL to a Blob */
function dataUrlToBlob(dataUrl: string): Blob {
  const [meta, b64] = dataUrl.split(",");
  const mime = /:([^;]+);/.exec(meta)?.[1] ?? "application/octet-stream";
  const bin = atob(b64);
  const arr = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) arr[i] = bin.charCodeAt(i);
  return new Blob([arr], { type: mime });
}

function inferExt(dataUrl: string): string {
  const m = /:([^;]+);/.exec(dataUrl);
  const mime = m?.[1] ?? "image/png";
  if (mime.includes("svg")) return "svg";
  if (mime.includes("jpeg") || mime.includes("jpg")) return "jpg";
  if (mime.includes("webp")) return "webp";
  return "png";
}

function safeName(s: string): string {
  return (s || "untitled")
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9-_]+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "")
    .slice(0, 50);
}

function buildColorsCss(palette: ColorPalette): string {
  return `/* ${palette.name} — ${palette.rationale} */
:root {
${palette.hexes.map((h, i) => `  --brand-${i + 1}: ${h};`).join("\n")}
}

.brand-bg { background: var(--brand-1); color: var(--brand-3); }
.brand-accent { color: var(--brand-2); }
.brand-accent-bg { background: var(--brand-2); color: var(--brand-1); }
.brand-surface { background: var(--brand-3); color: var(--brand-1); }
.brand-pop { color: var(--brand-4); }
`;
}

function buildColorsJson(palette: ColorPalette) {
  return JSON.stringify(
    {
      name: palette.name,
      rationale: palette.rationale,
      tokens: {
        base: palette.hexes[0],
        accent: palette.hexes[1],
        surface: palette.hexes[2],
        pop: palette.hexes[3],
      },
      hexes: palette.hexes,
    },
    null,
    2
  );
}

